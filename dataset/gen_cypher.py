import csv
import re
import secrets
import tarfile
import urllib.request
from dataclasses import dataclass
from pathlib import Path

DATASET_URL = "https://snap.stanford.edu/data/twitter.tar.gz"
DATASET_DIR = Path(__file__).resolve().parent

TWITTER_DIR = DATASET_DIR / "twitter"
EDGE_FILES_LIMIT = 15
NAMES_CSV = DATASET_DIR / "final_all_names_code.csv"

CYPHER_OUTPUT = DATASET_DIR / "populate_database.cypher"

@dataclass
class Profile:
    name: str
    email: str
    username: str
    password: str

@dataclass
class Node:
    id: int
    profile: Profile | None = None

@dataclass
class Edge:
    src: int
    dst: int

def ensure_dataset_exists() -> None:
    if TWITTER_DIR.exists():
        return

    archive_path = DATASET_DIR / "twitter.tar.gz"

    print(f"Downloading dataset from {DATASET_URL}...")
    urllib.request.urlretrieve(DATASET_URL, archive_path)

    print(f"Extracting {archive_path.name}...")
    with tarfile.open(archive_path, "r:gz") as archive:
        archive.extractall(DATASET_DIR)

    archive_path.unlink()
    print(f"Dataset extracted to {TWITTER_DIR}")


def _parse_edges_file(path: Path) -> list[tuple[int, int]]:
    out: list[tuple[int, int]] = []
    with path.open(encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if not line or line.startswith("#"):
                continue
            a, b = map(int, line.split()[:2])
            out.append((a, b))
    return out

def load_dataset() -> tuple[list[Node], list[Edge]]:
    if not TWITTER_DIR.exists():
        ensure_dataset_exists()

    node_ids: set[int] = set()
    edge_pairs: set[tuple[int, int]] = set()

    edge_files = sorted(TWITTER_DIR.glob("*.edges"))[:EDGE_FILES_LIMIT]
    for edge_file in edge_files:
        for a, b in _parse_edges_file(edge_file):
            node_ids.add(a)
            node_ids.add(b)
            edge_pairs.add((a, b))

    nodes = [Node(id=i) for i in sorted(node_ids)]
    edges = [Edge(src=s, dst=d) for s, d in sorted(edge_pairs)]
    return nodes, edges


def _slug(s: str) -> str:
    s = s.strip().lower()
    s = re.sub(r"[^a-z0-9]+", "_", s)
    s = s.strip("_")
    return s or "user"


def assign_profile_data(nodes: list[Node]) -> None:
    n = len(nodes)
    names: list[str] = []
    with NAMES_CSV.open(encoding="utf-8", newline="") as f:
        reader = csv.reader(f)
        next(reader, None)  # header: Name,Country_code,Country
        for row in reader:
            if len(names) >= n:
                break
            if not row:
                continue
            name = row[0].strip()
            if name:
                names.append(name)

    for node, name in zip(nodes, names, strict=True):
        base = _slug(name)
        node.profile = Profile(
            name=name,
            email=f"{base}.{node.id}@example.com",
            username=f"{base}_{node.id}",
            password=secrets.token_urlsafe(16),
        )


def _cypher_string_literal(s: str) -> str:
    escaped = s.replace("\\", "\\\\").replace("'", "\\'")
    return f"'{escaped}'"


def generate_cypher_queries(
    nodes: list[Node],
    edges: list[Edge]
) -> None:
    with CYPHER_OUTPUT.open("w", encoding="utf-8") as f:
        f.write("// Generated by gen_cypher.py\n\n")

        f.write("// 1.Create nodes\n")
        for node in nodes:
            p = node.profile
            f.write(f"""
                CREATE (n:User {{
                    id: {node.id}, 
                    name: {_cypher_string_literal(p.name)}, 
                    email: {_cypher_string_literal(p.email)}, 
                    username: {_cypher_string_literal(p.username)}, 
                    password: {_cypher_string_literal(p.password)}
                }});\n
            """)

        f.write("\n// 2. Create relationships\n")
        for edge in edges:
            f.write(f"""
                MATCH (a:User {{id: {edge.src}}}), (b:User {{id: {edge.dst}}})
                CREATE (a)-[:FOLLOWS]->(b);
            """)

# first, load the dataset
print("Loading dataset...")
nodes, edges = load_dataset()
print(f"Loaded {len(nodes)} nodes and {len(edges)} edges")

# assign profile data to nodes
print("Assigning profile data to nodes...")
assign_profile_data(nodes)

# generate cypher queries
print("Generating cypher queries...")
generate_cypher_queries(nodes, edges)

print(f"Wrote Cypher to {CYPHER_OUTPUT}")